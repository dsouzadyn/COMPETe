#include <bits/stdc++.h>

using namespace std;

long supow(long a, long b, long m) {
    if(b == 0) return 1;
    long sq = supow(a, b/2, m) % m;
    sq = (sq*sq) % m;

    if(b % 2 == 1) sq = (sq*a) % m;

    return sq % m;
}

