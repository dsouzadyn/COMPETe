#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "rt", stdin);
    #endif

    int n;
    int t;
    cin >> n;
    bool reports[n+1];
    memset(reports, false, sizeof reports);
    for(int i = 0; i < n; i++) {
        cin >> t;
        if(t == 0) continue;
        else reports[t] = true;
    }

    for(int i = 1; i <= n; i++) {
        if(!reports[i]) cout << i << " ";
    }

    return 0;
}

