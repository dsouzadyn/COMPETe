#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "r", stdin);
    #endif

    long a, b, n;
    int t;
    cin >> t;
    while(t--) {
        cin >> a >> b >> n;
        if(a < 0 && n % 2 == 0) a = -a;
        if(b < 0 && n % 2 == 0) b = -b;
        if(a == b || n == 0) cout << 0 << endl;
        else if (a > b) cout << 1 << endl;
        else cout << 2 << endl;
    }

    return 0; 
}

