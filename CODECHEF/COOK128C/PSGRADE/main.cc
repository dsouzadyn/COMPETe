#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "rt", stdin);
    #endif

    int t;
    cin >> t;
    while (t--) {
        int amin, bmin, cmin, a, b, c, tmin;
        cin >> amin >> bmin >> cmin >> tmin >> a >> b >> c;
        if ((a >= amin) && (b >= bmin) && (c >= cmin)) {
            if (a + b + c >= tmin) {
                cout << "YES";
            } else {
                cout << "NO";
            }
        } else {
            cout << "NO";
        }
        cout << endl;
    }

    return 0;
}