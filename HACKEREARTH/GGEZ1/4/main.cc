#include <bits/stdc++.h>

#define MOD 1000000007

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "r", stdin);
    #endif
    int t;
    long long n;
    cin >> t;
    while(t--) {
        cin >> n;
        long long c = 3;
        long long sum = 0;
        while(c < n) {
            sum = (sum + c) % MOD;
            cout << sum << endl;
            c <<= 1;
        }
        c = 4;
        while(c <= n) {
            sum = (sum + c|1) % MOD;
            cout << sum << endl;
            c <<= 1;
        }
        cout << sum << endl;
    }

    return 0; 
}

