#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "r", stdin);
    #endif

    int n;
    string s;

    cin >> n;
    for(int i = 0; i < n; i++) {
        cin >> s;
        int start = s.find('.');
        int end = s.find('.', start+1);
        int cnt = 0;
        int cnt2 = 0;
        for(int i = start+1; i < end; i++) {
            if(s[i] !='a' &&
                s[i] != 'e' &&
                s[i] != 'i' &&
                s[i] != 'o' &&
                s[i] != 'u') {
                cnt++;
            }
            cnt2++;
        }
        cout << cnt + 4 << "/" << cnt2 + 8 << endl;
    }

    return 0; 
}

