#include <bits/stdc++.h>

using namespace std;

long int phi(long int n) {
    float ans = n;
    for(int p = 2; p * p <= n; ++p) {
        if(n % p == 0) {
            while(n % p == 0)
                n /= p;
            ans *= (1.0 - (1.0/(float)p));
        }
    }
    if(n > 1)
        ans *= (1.0 - (1.0/ (float)n));
    return (long int) ans;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int n;
    cin >> n;
    cout << phi(n);
    return 0;
}

