#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "rt", stdin);
    freopen("out.txt", "wt", stdout);
    #endif

    string s;

    cin >> s;
    int opening = 0;
    int closing = 0;
    for(auto c : s) {
        if(c == '(') opening += 1;
        else if (opening) { 
            opening -= 1;
            closing += 1;
        }
    }

    cout << closing * 2 << endl;

    return 0; 
}

