#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "r", stdin);
    #endif

    int n, l;

    cin >> n >> l;

    vector<int> arr(n);

    for(int i = 0; i < n; i++) cin >> arr[i];

    sort(arr.begin(), arr.end());

    int max_dist = INT_MIN;

    for(int i = 1; i < n; i++) {
        max_dist = max(max_dist, arr[i] - arr[i-1]);
    }
    cout << fixed << setprecision(9);
    cout << max(max_dist/2.0, (double) max(arr[0], l - arr[n-1])) << endl;

    return 0; 
}

