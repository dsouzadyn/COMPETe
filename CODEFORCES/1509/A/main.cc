#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "rt", stdin);
    #endif

    int t;
    cin >> t;
    while(t--) {
        int n;
        cin >> n;
        vector<int> odd;
        vector<int> even;
        for(int i  = 0; i < n; i++) {
            int t;
            cin >> t;
            if ((t & 1) == 0) even.push_back(t);
            else odd.push_back(t);
        }
        for(auto u: odd) cout << u << " ";
        for(auto v: even) cout << v << " ";
        cout << endl;
    }

    return 0;
}