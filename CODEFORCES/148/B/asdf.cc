#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "rt", stdin);
    freopen("out.txt", "wt", stdout);
    #endif

    double vp, vd, t, f, c;

    cin >> vp >> vd >> t >> f >> c;

    double distp = vp * t;
    int count = 0;
    while(true) {
        double tr = distp/(vd-vp) + t;
        distp = vp * tr;
        // Chect if at time overtaken princess is at castle.
        cerr << distp << endl;
        if(distp >= c) break;
        // Else compute new t and find new distance from origin.
        // And increment count.
        t += (tr + f);
        distp = vp * t;
        count += 1;
    }

    cout << count << endl;

    return 0; 
}

