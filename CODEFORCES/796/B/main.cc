#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "r", stdin);
    #endif

    

    int n, m, k;
    cin >> n >> m >> k;
    bool holes[n+1];
    memset(holes, 0, sizeof holes);
    int t;
    for(int i = 0; i < m; i++) {
        cin >> t;
        holes[t] = true;
    }
    long u, v;
    int position = 1;
    for(int i = 0; i < k; i++) {
        cin >> u >> v;
        if(holes[position]) {
            cout << position << endl;
            return 0;
        }
        if (position == u) {
            position = v;
        } else if (position == v) {
            position = u;
        }
    }
    cout << position << endl;
    return 0; 
}

