#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "r", stdin);
    #endif

    int n;
    cin >> n;

    vector<int> arr(n);

    int sum = 0;
    int cost = 0;
    arr[0] = 0;
    for(int i = 1; i <= n; i++) cin >> arr[i];

    for(int i = 1; i <= n; i++) {
        sum += arr[i-1] - arr[i];
        if(sum < 0) {
            cost += abs(sum);
            sum = 0;
        }
    }

    cout << cost << endl;

    return 0; 
}

