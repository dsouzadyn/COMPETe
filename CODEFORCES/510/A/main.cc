#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "rt", stdin);
    #endif

    int n, m;
    cin >> n >> m;
    bool start = false;
    for(int i = 0; i < n; i++) {
        if(i % 2 != 0) {
            if(start) {
                cout << "#";
                for(int j = 0; j < m - 1; j++) cout << ".";
            } else {
                for(int j = 0; j < m - 1; j++) cout << ".";
                cout << "#";
            }
            cout << endl;
            start = !start;
        } else {
            for(int j = 0; j < m; j++) cout << "#";
            cout << endl;
        }
    }

    return 0;
}

