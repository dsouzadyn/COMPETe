#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "r", stdin);
    #endif

    int n, x;

    long long ans=0;

    cin >> n >> x;

    vector<long long> arr(n);
    for(int i = 0; i < n; i++) cin >> arr[i];

    sort(arr.begin(), arr.end());

    for(int i = 0; i < n; i++) {
        ans += (arr[i]*x);
        if(x > 1) x--;
    }

    cout << ans << endl;

    return 0; 
}

