#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    #ifndef ONLINE_JUDGE
    freopen("in.txt", "rt", stdin);
    #endif

    int n;
    cin >> n;
    vector<vector<int> > arr(n, vector<int>(n, 0));
    int c = 1;
    for(int i = 0; i < n; i++) {
        if (i % 2 == 0) {
            for(int j = 0; j < n; j++) {
                arr[j][i] = c;
                c++;
            }
        } else {
            for(int j = n - 1; j >= 0; j--) {
                arr[j][i] = c;
                c++;
            }
        }
        
    }
    for(int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cout << arr[i][j] << " ";
        }
        cout << endl;
    }

    return 0;
}